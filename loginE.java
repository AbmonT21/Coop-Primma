    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
    // TODO add your handling code here:
    String usuario = user_loginE.getText();
    String password = password_loginE.getText();
    String nombreRol = (String) jComboBox1.getSelectedItem();
    try {
        cx.getConnection();
        String MYSQL= "select r.rol_id, u.usuario, u.password from usuarios_roles r join usuarios u on u.usuario_id = r.usuario_id join roles rs on rs.rol_id = r.rol_id where u.usuario = ? and u.password = ? and rs.nombreRol = ?";
        PreparedStatement pstmt = con.prepareStatement(MYSQL);
        pstmt.setString(1, usuario);
        pstmt.setString(2, password);
        pstmt.setString(3, nombreRol);
        ResultSet rt = pstmt.executeQuery();
        if(rt.next()) {
            usuario = rt.getString("usuario");
            password = rt.getString("password");
            if(nombreRol.equals("Cajero")) {
                mainCajero cashierMenu = new mainCajero();
                cashierMenu.setVisible(true);
            } else if(nombreRol.equals("Ejecutivo")) {
                inicioEjecutivo executiveMenu = new inicioEjecutivo();
                executiveMenu.setVisible(true);
            }
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password", "Error", JOptionPane.ERROR_MESSAGE);
        }
          } catch(SQLException e) {
        e.printStackTrace();
    } catch (NullPointerException ex) {
        ex.printStackTrace();
    }                                       
}

