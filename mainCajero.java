    public String verMontoPrestamo(int loanID) {
    String cuotaprestamo = null;
    try {
        String query = "SELECT monto_cuota FROM prestamos WHERE prestamo_id = ?";
        PreparedStatement pstmt = con.prepareStatement(query);
        pstmt.setInt(1, loanID);
        ResultSet rs = pstmt.executeQuery();
        if(rs.next()) {
            cuotaprestamo = rs.getString("monto_cuota");
        }
        
    } catch(SQLException e) {
        e.printStackTrace();
    }
    return cuotaprestamo;
}
