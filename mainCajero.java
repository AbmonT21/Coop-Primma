    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String accountNumber = jTextField1.getText();
    double depositAmount = Double.parseDouble(jTextField3.getText());
    
    try {
        // revisa si la cuenta existe y devuelve el balance actual
        String query = "SELECT balance FROM cuentas WHERE cuenta_id = ?";
        PreparedStatement pstmt = con.prepareStatement(query);
        pstmt.setString(1, accountNumber);
        ResultSet rs = pstmt.executeQuery();
        if (rs.next()) {
            double currentBalance = rs.getDouble("balance");
            double newBalance = currentBalance + depositAmount;
            
            // Update del balance de la cuenta
            String updateQuery = "UPDATE cuentas SET balance = ? WHERE cuenta_id = ?";
            PreparedStatement updatePstmt = con.prepareStatement(updateQuery);
            updatePstmt.setDouble(1, newBalance);
            updatePstmt.setString(2, accountNumber);
            updatePstmt.executeUpdate();
            
        String insertQuery = "INSERT INTO cuentas_transacciones (cuenta_id, tipo_transaccion_id, monto) VALUES (?, ?, ?)";
        PreparedStatement insertStatement = con.prepareStatement(insertQuery);
        insertStatement.setString(1, accountNumber);
        insertStatement.setInt(2, 4); // 4 es el tipo de transaccion para los depositos
        insertStatement.setDouble(3, depositAmount);
        insertStatement.executeUpdate();
            
           
            JOptionPane.showMessageDialog(this, "Se han depositado RD$" + depositAmount + " exitosamente. El nuevo balance es: $" + newBalance);
        } else {
            
            JOptionPane.showMessageDialog(this, "Cuenta no existe", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    } catch(SQLException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Un error ha ocurrido al intentar hacer el deposito.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

