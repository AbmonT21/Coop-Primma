    private void retiro() {
    String accountNumber = jTextField4.getText();
    double amount = Double.parseDouble(jTextField6.getText());
    
    try {
        
        String checkAccountType = "SELECT id_tipocuenta FROM cuentas WHERE cuenta_id=?";
        PreparedStatement checkAccountTypeStmt = con.prepareStatement(checkAccountType);
        checkAccountTypeStmt.setString(1, accountNumber);
        ResultSet rt = checkAccountTypeStmt.executeQuery();
        if (rt.next() && rt.getInt("id_tipocuenta") == 1) {
            JOptionPane.showMessageDialog(this, "No se puede retirar. Esta es una cuenta de aportaciones.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        PreparedStatement pstmt = con.prepareStatement("SELECT balance FROM cuentas WHERE cuenta_id = ?");
        pstmt.setString(1, accountNumber);
        ResultSet rs = pstmt.executeQuery();
        
        if (rs.next()) {
            double currentBalance = rs.getDouble("balance");
            if (currentBalance - amount >= 300) {
                PreparedStatement updatePstmt = con.prepareStatement("UPDATE cuentas SET balance = ? WHERE cuenta_id = ?");
                updatePstmt.setDouble(1, currentBalance - amount);
                updatePstmt.setString(2, accountNumber);
                int rowsUpdated = updatePstmt.executeUpdate();
                
            String insertQuery = "INSERT INTO cuentas_transacciones (cuenta_id, tipo_transaccion_id, monto) VALUES (?, ?, ?)";
            PreparedStatement insertStatement = con.prepareStatement(insertQuery);
            insertStatement.setString(1, accountNumber);
            insertStatement.setInt(2, 3);
            insertStatement.setDouble(3, amount);
            insertStatement.executeUpdate();
                
                if (rowsUpdated > 0) {
                    JOptionPane.showMessageDialog(this, "Se han retirado RD$" + amount + " exitosamente.");
                } else {
                    JOptionPane.showMessageDialog(this, "Error de retiro.");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Fondos Insuficientes");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Cuenta no existe.");
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }
}

    
