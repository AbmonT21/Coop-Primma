public double getCurrentBalance(int accountId) throws SQLException {
    String query = "SELECT balance FROM cuentas WHERE cuenta_id = ? AND id_tipocuenta = 1";
    try (PreparedStatement pstmt = con.prepareStatement(query)) {
        pstmt.setInt(1, accountId);
        try (ResultSet rs = pstmt.executeQuery()) {
            return rs.next() ? rs.getDouble("balance") : 0.0;
        }
    }
}
public boolean verTipocuenta(int accountId) {
    boolean isAportaciones = false;
    try {
        String query = "SELECT id_tipocuenta FROM cuentas WHERE cuenta_id = ?";
        PreparedStatement pstmt = con.prepareStatement(query);
        pstmt.setInt(1, accountId);
        ResultSet rs = pstmt.executeQuery();
        if(rs.next()) {
            int accountType = rs.getInt("id_tipocuenta");
            if(accountType == 1) {
                isAportaciones = true;
                }
            else 
                  JOptionPane.showMessageDialog(null, "Cuenta no existe o no es una cuenta de aportaciones", "Error", JOptionPane.ERROR_MESSAGE);
            }   
                 
    } catch(SQLException e) {
        e.printStackTrace();
          
    }
    return isAportaciones;
}

